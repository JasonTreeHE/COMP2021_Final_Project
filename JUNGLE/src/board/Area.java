package board;
/**
 * This class defines four different areas in a gameboard.
 *
 * @author incandescentxxc
 * @version 1.0
 */
public enum Area {


    /**
     * literal meaning.
     */
    NORMAL
    ,
    /**
     * the property of the unit is river.
     */
    RIVER
    ,
    /**
     * the property is trap.
     */
    TRAP
    ,



    /**
     * type is the value of four types:normal, river,trap,den.
     */
    DEN


}
